    /*
    
        *Method: setCopyListByBook
        *Rerurn: void
        *Parameters: Book object
    
     */
    private void setCopyListByBook(Book book1) {
        //set copy list
        this.copyModel.clear();

        this.copies = this.copyServices.getAllCopiesByBook(book1);
        if (this.copies.size() > 0) {
            for (Copy copyItem : this.copies) {
                String item = copyItem.getId() + CommonConstants.STRING_SEPERATOR_LISTS
                        + copyItem.getIsbn() + CommonConstants.STRING_SEPERATOR_LISTS
                        + copyItem.getState();
                this.copyModel.addElement(item);
            }
        }

        this.copyList.setModel(this.copyModel);
    }

    /*
    
        *Method: setBookListByKey
        *Rerurn: void
        *Parameters: String value
    
     */
    private void setBookListByKey(String key) {
        this.books = bookServices.searchBookByName(key);
        this.bookModel.clear();

        if (this.books.size() > 0) {

            for (Book bookItem : this.books) {
                String item = bookItem.getName() + CommonConstants.STRING_SEPERATOR_LISTS + bookItem.getIsbn();
                this.bookModel.addElement(item);
            }

        }

        this.bookList.setModel(bookModel);
    }

