    private void deleteBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBookBtnActionPerformed
        int respond;
        String isbn = this.isbn.getText();
        this.book = this.bookServices.getBookByIsbn(isbn);

        //check copies
        this.copies.clear();
        this.copies = this.copyServices.getAllCopiesByBook(this.book);

        if (this.copies.size() > 0) {
            respond = JOptionPane.showConfirmDialog(null, this.copies.size() + " copies will be Removed\nDo you want to remove all copies?",
                    "Select",
                    JOptionPane.YES_NO_OPTION);

            if (respond == CommonConstants.CONFIRM_MESSAGE_YES) {
                //Yes selected
                int check = this.copyServices.deleteCopiesByBook(this.book);

                if (check == CommonConstants.OPRTATOION_SUCCESS) {
                    JOptionPane.showMessageDialog(null, "All the " + this.copies.size() + " copies of " + this.book.getName() + " were deleted.");
                    this.copies.clear();
                } else if (check == CommonConstants.OPERATION_FAILED) {
                    JOptionPane.showMessageDialog(null, "Unknown error occurred.");
                }

                //Delete book
                check = this.bookServices.deleteBook(this.book);

                if (check == CommonConstants.OPRTATOION_SUCCESS) {
                    JOptionPane.showMessageDialog(null, "The book: " + this.book.getName() + " deleted successfully");

                    resetBookForm();
                    resetCopyForm();
                    this.bookModel.clear();
                    this.copyModel.clear();

                } else if (check == CommonConstants.OPERATION_FAILED) {
                    JOptionPane.showMessageDialog(null, "Unknown error occurred.");
                }

            }

        } else {

            //No copies found
            //Delete book
            int check = this.bookServices.deleteBook(this.book);

            if (check == CommonConstants.OPRTATOION_SUCCESS) {
                JOptionPane.showMessageDialog(null, "The book: " + this.book.getName() + " deleted successfully");
                //clear inputs
                this.isbn.setText("");
                this.name.setText("");
                this.subject.setText("");
                this.author.setText("");
                this.publisher.setText("");
                this.cost.setText("");
                this.copyIsbnTxt.setText("");
                this.copyNameTxt.setText("");

            } else if (check == CommonConstants.OPERATION_FAILED) {
                JOptionPane.showMessageDialog(null, "Unknown error occurred.");
            }

            this.copyModel.clear();
            setBookListByKey(this.key.getText());

        }

        //Delete book
    }//GEN-LAST:event_deleteBookBtnActionPerformed

