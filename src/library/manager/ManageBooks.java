    private void addBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookBtnActionPerformed

        //assign inputs to variables
        String isbnNo = isbn.getText();
        String bookName = name.getText();
        String bookSubject = subject.getText();
        String bookAuthor = author.getText();
        String bookPublisher = publisher.getText();
        double bookCost = Double.parseDouble(cost.getText());

        //set attributes in new book object
        this.book.setIsbn(isbnNo);
        this.book.setName(bookName);
        this.book.setSubject(bookSubject);
        this.book.setAuthor(bookAuthor);
        this.book.setPublisher(bookPublisher);
        this.book.setCost(bookCost);

        int check = bookServices.saveBook(this.book);

        // try to update if the book not added
        if (check == CommonConstants.OPERATION_FAILED) {
            check = this.bookServices.updateBook(book);
        }

        if (check == CommonConstants.OPRTATOION_SUCCESS) {

            resetBookForm();

            //Display success message.
            JOptionPane.showMessageDialog(null, "Book saved successfully.",
                    "Data inserted.",
                    2);

            //save 1st copy
            Copy copy = new Copy();
            copy.setIsbn(this.book.getIsbn());
            copy.setState(CommonConstants.COPY_STATE_AVAILABLE);
            this.copyServices.saveCopy(copy);

            setBookListByKey(this.book.getName());

        } else if (check == CommonConstants.OPERATION_FAILED) {
            //Display faliure message
            JOptionPane.showMessageDialog(null, "Cannot add this book.",
                    "Data not inserted.",
                    2);
        }

    }//GEN-LAST:event_addBookBtnActionPerformed

